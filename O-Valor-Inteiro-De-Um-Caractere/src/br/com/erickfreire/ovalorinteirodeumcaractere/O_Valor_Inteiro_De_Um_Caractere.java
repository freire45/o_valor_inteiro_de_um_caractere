package br.com.erickfreire.ovalorinteirodeumcaractere;

public class O_Valor_Inteiro_De_Um_Caractere {

	public static void main(String[] args) {
		
		System.out.println("Programa que exibe o valor inteiro de um caractere de acordo com a tabela ASCII: \n");
		
		System.out.printf("O caractere %c têm o valor %d%n", 'A', ((int) 'A'));
		System.out.printf("O caractere %c têm o valor %d%n", 'B', ((int) 'B'));
		System.out.printf("O caractere %c têm o valor %d%n", 'C', ((int) 'C'));
		System.out.printf("O caractere %c têm o valor %d%n", 'a', ((int) 'a'));
		System.out.printf("O caractere %c têm o valor %d%n", 'b', ((int) 'b'));
		System.out.printf("O caractere %c têm o valor %d%n", 'c', ((int) 'c'));
		System.out.printf("O caractere %c têm o valor %d%n", '0', ((int) '0'));
		System.out.printf("O caractere %c têm o valor %d%n", '1', ((int) '1'));
		System.out.printf("O caractere %c têm o valor %d%n", '2', ((int) '2'));
		System.out.printf("O caractere %c têm o valor %d%n", '$', ((int) '$'));
		System.out.printf("O caractere %c têm o valor %d%n", '*', ((int) '*'));
		System.out.printf("O caractere %c têm o valor %d%n", '+', ((int) '+'));
		System.out.printf("O caractere %c têm o valor %d%n", '/', ((int) '/'));
		System.out.printf("O caractere %c têm o valor %d%n", ' ', ((int) ' '));
	}

}
